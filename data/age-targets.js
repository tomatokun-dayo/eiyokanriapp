// - 目安量(AI)は「不足しにくい量」であり、下回っても直ちに不足を意味しない（推奨量とは性質が異なる）。
// - 家庭内の参考であり、医学的・栄養学的な指導値ではない。
// - この設計は「飲んだ量が分かること」が前提。母乳は量を測れないため、母乳中心の場合は合計が過小評価になる。
