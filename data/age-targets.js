//   年齢区分も食事摂取基準の区分に合わせてあるため、目安値は公式値と1対1で対応する。
//   男女で値が異なるものは中間値を採用（このアプリは性別を持たないため）。
//
// 2020年版からの主な変更:
//   - 鉄 6～11か月: 男5.0/女4.5mg → 男女とも4.5mg（推奨量）に統一
//   - 鉄 1～2歳:   4.5mg → 4.0mg（推奨量）
